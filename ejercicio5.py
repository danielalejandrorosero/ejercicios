"""
5) Calcular el nuevo salario de un obrero si obtuvo un incremento del 25% sobre su salario
anterior.
"""

salario_anterior = float(input('Ingrese su salario anterior: '))

aumento = salario_anterior * 0.25

salario_nuevo = salario_anterior + aumento




print(f'el nuevo salario es: {salario_nuevo}')